# double quotes
course = "python's Course for Beginners"
print(course)

# single quotes
course = 'python "Course" for Beginners'
print(course)

# triple quotes
course = ''' 

This is a multi line string.
I am learning Python.
This is fun!

'''

# accessing characters in a string
course = "python for Beginners"

# indexing 0 = p
print(course[0])

# indexing -1 = s
print(course[-1])

# first 3 characters
print(course[0:3])


name = "Jennifer"
# all but first and last character will be printed
print(name[1:-1])