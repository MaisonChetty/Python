import math

# round function example 
x = 2.9
print(round(x))

# abs function example
print(abs(-7.25))

# ceil function example
print(math.ceil(x))

# floor function example
print(math.floor(x))

# sqrt function example
print(math.sqrt(16))

# pow function example
print(pow(3, 4))

# exp function example
print(math.exp(2))

# log function example
print(math.log(100, 10))

# log10 function example
print(math.log10(1000))

# sin function example
print(math.sin(math.pi / 2))

# cos function example
print(math.cos(0))

# tan function example
print(math.tan(math.pi / 4))

# factorial function example
print(math.factorial(5))

# gcd function example
print(math.gcd(48, 18))

# lcm function example (available in Python 3.9+)
print(math.lcm(12, 15))

# degrees function example
print(math.degrees(math.pi))

# radians function example
print(math.radians(180))

# hypot function example
print(math.hypot(3, 4))

# modf function example
print(math.modf(5.75))

# fmod function example
print(math.fmod(20, 3))

# isfinite function example
print(math.isfinite(10.5))
